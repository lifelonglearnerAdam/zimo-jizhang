def write_article(
    id_: str,
    title: str,
    category: str,
    icon: str,
    minutes: int,
    summary: str,
    key_points: list[str],
    action_tip: str,
    tags: list[str],
    sections: list[tuple[str, list[str]]],
    priority: int = 100,
) -> dict:
    fm_points = "\n".join(f"  - {p}" for p in key_points)
    tags_s = "[" + ", ".join(tags) + "]"
    body_parts: list[str] = []
    for h, paras in sections:
        body_parts.append(f"## {h}\n")
        for p in paras:
            body_parts.append(p.strip() + "\n")
    body = "\n".join(body_parts).strip() + "\n"
    text = f"""---
id: {id_}
title: {title}
category: {category}
icon: {icon}
minutes: {minutes}
summary: {summary}
key_points:
{fm_points}
action_tip: {action_tip}
tags: {tags_s}
published_at: 2026-07-23
updated_at: 2026-07-23
priority: {priority}
---

{body}"""
    (ART / f"{id_}.md").write_text(text, encoding="utf-8")
    return {
        "id": id_,
        "path": f"articles/{id_}.md",
        "title": title,
        "category": category,
        "icon": icon,
        "minutes": minutes,
        "summary": summary,
        "published_at": "2026-07-23",
        "updated_at": "2026-07-23",
        "priority": priority,
    }


